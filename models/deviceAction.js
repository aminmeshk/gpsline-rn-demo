class DeviceAction {
  constructor(id, name, description, iconType, iconName, color, textAsIcon) {
    this.id = id;
    this.name = name;
    this.description = description;
    this.iconType = iconType;
    this.iconName = iconName;
    this.color = color;
    this.textAsIcon = textAsIcon;
  }
}

export default DeviceAction;
